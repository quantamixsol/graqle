def _make_node(
    label: str,
    entity_type: str = "Function",
    description: str = "",
    signature: str | None = None,
) -> MagicMock:
    node = MagicMock()
    node.label = label
    node.entity_type = entity_type
    node.description = description
    node.properties = {"signature": signature} if signature else {}
    return node


def _make_server(nodes: dict[str, MagicMock]) -> KogniDevServer:
    srv = KogniDevServer.__new__(KogniDevServer)
    srv.config_path = "graqle.yaml"
    srv.read_only = False
    srv._config = MagicMock()
    srv._graph_file = "graqle.json"
    srv._graph_mtime = 9999999999.0

    graph = MagicMock()
    graph.nodes = nodes
    graph.edges = {}
    graph._activate_subgraph = MagicMock(return_value=list(nodes.keys()))
    graph.config.activation.strategy = "spread"

    backend = MagicMock()
    gen_result = MagicMock()
    gen_result.text = (
        "--- a/t.py\n+++ b/t.py\n@@ -1,1 +1,2 @@\n+# gen\n\nSUMMARY: Done."
    )
    gen_result.tokens_used = 100
    backend.generate = AsyncMock(return_value=gen_result)
    backend.cost_per_1k_tokens = 0.003
    graph._get_backend_for_node = MagicMock(return_value=backend)
    srv._graph = graph
    return srv


async def _run_generate(server, description, file_path=None):
    args = {"description": description}
    if file_path:
        args["file_path"] = file_path
    with patch("graqle.cloud.credentials.load_credentials") as mc:
        mc.return_value = MagicMock(plan="team")
        with patch.object(server, "_handle_preflight", new=AsyncMock(
            return_value=json.dumps({
                "risk_level": "low", "warnings": [], "lessons": [],
                "safety_boundaries": [], "adrs": [],
            })
        )):
            with patch.object(server, "_handle_safety_check", new=AsyncMock(
                return_value=json.dumps({"overall_risk": "low"})
            )):
                # Allow tmp_path files past the project-root check
                with patch.object(
                    server, "_resolve_file_path",
                    side_effect=lambda p: p,
                ):
                    return json.loads(await server._handle_generate(args))


def _get_prompt(server) -> str:
    backend = server._graph._get_backend_for_node.return_value
    return backend.generate.call_args[0][0]


