# ---------------------------------------------------------------------------

class TestOT056PrePublish:

    @pytest.mark.asyncio
    async def test_generate_kg_signature_happy_path(self) -> None:
        """KG Function node carries full signature → exact params in prompt."""
        node = _make_node(
            "process", "Function", "Process incoming data",
            signature="def process(data: list[str], threshold: float = 0.5) -> bool",
        )
        server = _make_server({"process": node})
        await _run_generate(server, "add validation to process function")
        prompt = _get_prompt(server)

        assert "data: list[str]" in prompt
        assert "threshold: float" in prompt
        assert "EXACT parameter names" in prompt

