    # -----------------------------------------------------------------------
    # Test 7 — Malformed source AST resilience
    # -----------------------------------------------------------------------

    @pytest.mark.asyncio
    async def test_generate_malformed_source_ast_resilience(self, tmp_path: Path) -> None:
        """Broken .py file → AST fallback fails gracefully, generation completes."""
        target = tmp_path / "broken.py"
        broken_source = "def broken(x: int,\n    pass\n"
        target.write_text(broken_source, encoding="utf-8")

        # Confirm genuinely broken
        with pytest.raises(SyntaxError):
            ast.parse(broken_source)

        node = _make_node("broken", "Function", "A broken function")
        server = _make_server({"broken": node})
        result = await _run_generate(server, "fix syntax", file_path=str(target))

        # Must complete without crash
        assert "error" not in result or result.get("error") is None
        # Backend was still called
        backend = server._graph._get_backend_for_node.return_value
        assert backend.generate.called
